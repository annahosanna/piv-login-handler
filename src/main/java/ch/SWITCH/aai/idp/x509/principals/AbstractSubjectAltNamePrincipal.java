package ch.SWITCH.aai.idp.x509.principals;

import java.security.Principal;

public abstract class AbstractSubjectAltNamePrincipal implements Principal {

	private String name;
	
	public AbstractSubjectAltNamePrincipal(Object object) {
		this.name = object.toString();
	}

	public AbstractSubjectAltNamePrincipal(byte[] data) {
		this.name = "DER ASN String";
	}

	public String getName() {
		return name;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AbstractSubjectAltNamePrincipal other = (AbstractSubjectAltNamePrincipal) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	public String toString() {
		return name;
	}
}
